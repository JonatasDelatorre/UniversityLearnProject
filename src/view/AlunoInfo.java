    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            ArrayList<objNotas> objList = aluno.getHistorico();
            String format = "";
            for(int i = 0; i < objList.size(); i++ ){
                if(objList.get(i).getMateria().getNome().equals(String.valueOf(comboBoxMateria.getSelectedItem()))){
                    textArea.setText(String.valueOf(comboBoxMateria.getSelectedItem()) + "\n" + objList.get(i).toString());
                }
            }
            textArea.setText(format);
        }
        catch(NullPointerException nullPointerException){
            JOptionPane.showMessageDialog(null, "Erro ao encontrar informações", "ERRO", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


