    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            aluno = uniPrincipal.getAluno(textMatricula.getText());
            ArrayList<String> matList = aluno.getMaterias();
            for(int i = 0; i < matList.size(); i++ ){
                comboBoxMateria.addItem(matList.get(i));
            }
        }
        catch(NullPointerException nullPointerException){
            JOptionPane.showMessageDialog(null, "Erro ao encontrar informações", "ERRO", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            ArrayList<String> matList = aluno.getMaterias();
            String format = "";
            for(int i = 0; i < matList.size(); i++ ){
                format = format + matList.get(i) + "\n";
            }
            textArea.setText(format);
        }
        catch(NullPointerException nullPointerException){
            JOptionPane.showMessageDialog(null, "Erro ao encontrar informações", "ERRO", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            ArrayList<objNotas> objList = aluno.getHistorico();
            String format = "";
            for(int i = 0; i < objList.size(); i++ ){
                format = format + objList.get(i).getMateria().getNome() + "\n" + objList.get(i).toString() + "\n\n";
            }
            textArea.setText(format);
        }
        catch(NullPointerException nullPointerException){
            JOptionPane.showMessageDialog(null, "Erro ao encontrar informações", "ERRO", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

