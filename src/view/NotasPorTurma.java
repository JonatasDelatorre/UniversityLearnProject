    private void comboBoxMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxMatActionPerformed
        try{
            String cod = String.valueOf(comboBoxMat.getSelectedItem());
            for(int i = 0; i < matList.size(); i++){
                if(matList.get(i).getCodMateria().equals(cod)){
                    labelMatShow.setText(matList.get(i).getNome());
                    this.matPrincipal = matList.get(i);
                }
            }

            comboBoxAluno.removeAllItems();
            comboBoxAluno.addItem("Aluno");

            
            ArrayList<Aluno> alunos = matPrincipal.getAlunoList();
            for(int i = 0; i < alunos.size(); i++){
                comboBoxAluno.addItem(alunos.get(i).getMatricula());
            }
        }
        catch(NullPointerException nullPointerException){
            labelMatShow.setText("ERRO");
        }
    }//GEN-LAST:event_comboBoxMatActionPerformed

    private void comboBoxAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxAlunoActionPerformed
        try{     
            String cod = String.valueOf(comboBoxAluno.getSelectedItem());
            labelAlunoShow.setText(uniPrincipal.getAluno(cod).getNome());
            String notas = uniPrincipal.getAluno(cod).getNotaMat(matPrincipal.getCodMateria());
            textArea.setText(notas);
       }
       catch(NullPointerException nullPointerException){
            labelAlunoShow.setText("Nome");
       }
    }//GEN-LAST:event_comboBoxAlunoActionPerformed


